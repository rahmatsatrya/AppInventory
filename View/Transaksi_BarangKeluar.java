    private void simpanData() {
        
            String noBarMas        = t_noBarangMasuk.getText();
            String kode_barang     = t_kodeBarang.getText();
            String naBar           = t_namaBarang.getText();
            String tgl_masuk       = t_tanggal.getText();
            String id_peng         = lb_id.getText();
            
            String text = lb_total.getText(); // Mengambil teks dari label
            // Menghapus karakter non-numerik, kecuali untuk angka
            String numericString = text.replaceAll("[^0-9]", "");
            Long total             = Long.parseLong(numericString);
            
            
            model_pengguna user = new model_pengguna();
            model_barang brg = new model_barang();
            model_barangKeluar barKel = new model_barangKeluar();
            model_detBarangKeluar detBarKel = new model_detBarangKeluar();
            
            // menyimpan tabel barang masuk
            barKel.setNo_keluar(noBarMas);
            barKel.setTgl_keluar(tgl_masuk);
            barKel.setTotal_keluar(total); 
            
            // pengguna
            user.setId_pengguna(id_peng);
            barKel.setMod_pengguna(user);
            
            // menyimpan detail barang Keluar
            detBarKel.setNo_keluar(barKel);
            detBarKel.setKode_barang(brg);
            
            
            servisBarKel.tambahData(barKel);
            servisDetBarKel.tambahData(detBarKel);
            servisDetBarKel.hapusSementara(detBarKel);
            tblModelKeluar.tambahData(barKel);
            tampilPanel();
            loadData();
            resetForm();
            
    }
    
    
