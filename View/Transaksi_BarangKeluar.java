    private void PerbaruiDataSementara() {
        int index = tbl_sementara.getSelectedRow();
        if (index != -1){
            model_chartBarangKeluar chartBarKel = tblModelChartBarKel.getData(tbl_sementara.convertRowIndexToModel(index));
            
                String kode = t_kodeBarang.getText();
                String nama = t_namaBarang.getText();
                Long harga = Long.parseLong(t_harga.getText());
                int jumlah = Integer.parseInt (t_jumlah.getText());
                Long subtotal = Long.parseLong(t_subTotal.getText());
                
                model_barang brg = new model_barang();
                model_chartBarangKeluar mod_chartBarKel = new model_chartBarangKeluar();
                model_detBarangKeluar mod_detBarKel = new model_detBarangKeluar();
                
                brg.setKode_barang(kode);
                brg.setNama_barang(nama);
                brg.setHarga(harga);
                mod_detBarKel.setJml_keluar(jumlah);
                mod_detBarKel.setSubtotal_keluar(subtotal);
                
                mod_chartBarKel.setMod_barang(brg);
                mod_chartBarKel.setMod_detKeluar(mod_detBarKel);
                
                servisChartBarKel.perbaruiData(mod_chartBarKel);
                tblModelChartBarKel.perbaruiData(index, mod_chartBarKel);
                
                servisDetBarKel.sumTotal(mod_detBarKel);
                lb_total.setText("Rp. " + mod_detBarKel.getSubtotal_keluar().toString()); // label subtotal
        
                loadDataSementara();
                bersihBarang();
                resetForm();
            }
    }

    private void hapusDataChartPes() {
        int index = tbl_sementara.getSelectedRow();
        if (index != -1){
            model_chartBarangKeluar chartBarKel = tblModelChartBarKel.getData(tbl_sementara.convertRowIndexToModel(index));
            if(JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus data?","Konfirmasi",JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
                servisChartBarKel.hapusData(chartBarKel);
                tblModelChartBarKel.hapusData(index);
                loadDataSementara();
                resetForm();
                bersihBarang();
                model_detBarangKeluar detBarKel = new model_detBarangKeluar();
                servisDetBarKel.sumTotal(detBarKel);
                lb_total.setText("Rp. " + detBarKel.getSubtotal_keluar().toString()); // label subtotal
            }
        }else {
            JOptionPane.showMessageDialog(null, "Pilih record yang akan dihapus!");
        }
        loadDataSementara();
        resetForm();
        bersihBarang();
        model_detBarangKeluar detBarKel = new model_detBarangKeluar();
        servisDetBarKel.sumTotal(detBarKel);
        lb_total.setText("Rp. " + detBarKel.getSubtotal_keluar().toString()); // label subtotal
    }

