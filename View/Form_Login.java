//    method proses login
    private void prosesLogin(){
        if (validasiInput() == true){
            String id = t_username.getText();
            String user = t_username.getText();
            String pass = t_password.getText();
            
            model_login model_in = new model_login();
            
            model_in.setId_user(id);
            model_in.setUsername(user);
            model_in.setPass(pass);
            
            servis.prosesLogin(model_in);
            if (tutup){
                dispose();
            }
        }
    }
    
//    method proses validasi
    private boolean validasiInput(){
        boolean valid = false;
        if (t_username.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
        }else if(t_password.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        }else{
            valid = true;
        }
        return valid;
    }
    
    


